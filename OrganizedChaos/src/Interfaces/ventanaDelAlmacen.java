    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        String id = campoID.getText();
        
        if ("Almacen A".equals(id)){
           objeto.eliminarAlmacenA();
        }
        else if ("Almacen B".equals(id)){
           objeto.eliminarAlmacenB();
        }
        else if ("Almacen C".equals(id)){
           objeto.eliminarAlmacenC();
        }
        else if ("Almacen D".equals(id)){
           objeto.eliminarAlmacenD();
        }
        else if ("Almacen E".equals(id)){
           objeto.eliminarAlmacenE();
        }
        else{
            JOptionPane.showMessageDialog(null, "El almacen introducido no existe");
        }
        
        objeto.leerDatosA();
        objeto.leerDatosB();
        objeto.leerDatosC();
        objeto.leerDatosD();
        objeto.leerDatosE();
        //objeto.escribirRutas(); 
        objeto.leerRutas();
        
        campoID.setText(null);

    }//GEN-LAST:event_btnEliminarActionPerformed

